        /// <summary>
        /// Decrypts XOR-encrypted bytes back to a string.
        /// </summary>
        private static string D(byte[] encrypted)
        {
            var result = new byte[encrypted.Length];
            for (int i = 0; i < encrypted.Length; i++)
                result[i] = (byte)(encrypted[i] ^ _k[i % _k.Length]);
            return Encoding.ASCII.GetString(result);
        }

        /// <summary>
        /// Utility: encrypts a plaintext string for embedding.
        /// Call this at design time to generate new byte arrays.
        /// Example: SecurityStrings.Encrypt("MyClassName") → byte array literal
        /// </summary>
        internal static byte[] Encrypt(string plaintext)
        {
            var bytes = Encoding.ASCII.GetBytes(plaintext);
            var result = new byte[bytes.Length];
            for (int i = 0; i < bytes.Length; i++)
                result[i] = (byte)(bytes[i] ^ _k[i % _k.Length]);
            return result;
        }
